# ---- PYTHON VIRTUAL ENVIRONMENT -----

# Install virtualenv package if you donn;t have it
# pip install virtualenv

# Create a new folder for your project and navigate to it in the terminal, then create a  virtual environment using the following command:
# python -m venv venv (or any name you want to give to your virtual environment folder)

# Acrivate the virtual environment
# venv/Scipts/activate


# Once the virtual environment is activated, you can install packages using pip and they will be installed in the virtual environment, not globally

# To check which packages are installed in the virtual environment, you can use the command:
# pip list or pip freeze

# To deactivate the virtual environment and go back to the global environment to continue your project, simply run the command:
# deactivate



